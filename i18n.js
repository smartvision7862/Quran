let currentLang = "en";

// Dynamic Google Translate API client-side translator
async function translateText(text, targetLang) {
  if (targetLang === 'en' || !text) return text;
  try {
    const url = `https://translate.googleapis.com/translate_a/single?client=gtx&sl=auto&tl=${targetLang}&dt=t&q=${encodeURIComponent(text)}`;
    const response = await fetch(url);
    if (!response.ok) return text;
    const json = await response.json();
    const translatedParts = json[0].map(item => item[0]);
    return translatedParts.join('');
  } catch (err) {
    console.error("Translation helper error:", err);
    return text;
  }
}

function detectBrowserLang() {
  const saved = localStorage.getItem("quran_lang");
  if (saved) return saved;
  const navLang = (navigator.language || navigator.languages?.[0] || "en").slice(0, 2);
  const supported = ["en", "ur", "ar", "hi", "bn", "pa", "ta", "te", "mr", "gu", "ml", "kn", "tr", "id", "fr", "es", "de", "ru", "fa", "zh", "ja", "pt", "it"];
  return supported.includes(navLang) ? navLang : "en";
}

async function applyLanguage(lang) {
  currentLang = lang;
  window.currentLang = lang;
  localStorage.setItem("quran_lang", lang);

  // Set page direction based on language
  const isRtl = (lang === 'ur' || lang === 'ar' || lang === 'fa');
  document.body.classList.toggle('rtl-dir', isRtl);
  document.documentElement.setAttribute('dir', isRtl ? 'rtl' : 'ltr');
  document.documentElement.setAttribute('lang', lang);

  // Sync selectors
  const langSelect = document.getElementById("langSelect");
  if (langSelect) langSelect.value = lang;
  const appLangSelect = document.getElementById("appInterfaceLang");
  if (appLangSelect) appLangSelect.value = lang;

  // Translate all DOM elements with data-i18n
  const elements = document.querySelectorAll("[data-i18n]");
  for (const el of elements) {
    const key = el.getAttribute("data-i18n");
    const entry = i18n[key];
    if (entry) {
      if (entry[lang]) {
        if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
          el.placeholder = entry[lang];
        } else {
          el.innerHTML = entry[lang];
        }
      } else {
        // Fallback to dynamic google translate
        const englishText = entry['en'];
        if (englishText) {
          const translated = await translateText(englishText, lang);
          if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
            el.placeholder = translated;
          } else {
            el.innerHTML = translated;
          }
        }
      }
    }
  }

  // Update page title dynamically
  const hash = window.location.hash || '#dashboard';
  const titleKey = {
    '#dashboard': 'nav.dashboard',
    '#sabaq': 'nav.sabaq',
    '#hadith': 'nav.hadith',
    '#names': 'nav.names',
    '#bookmarks': 'nav.bookmarks',
    '#chatbot': 'nav.chatbot',
    '#settings': 'nav.settings'
  }[hash] || 'nav.dashboard';

  const titleEntry = i18n[titleKey];
  if (titleEntry) {
    let titleText = titleEntry[lang];
    if (!titleText && titleEntry['en']) {
      titleText = await translateText(titleEntry['en'], lang);
    }
    if (titleText) {
      const pageTitleEl = document.getElementById('pageTitle');
      if (pageTitleEl) pageTitleEl.textContent = titleText;
    }
  }

  // Refresh status text
  const statusText = document.getElementById("statusText");
  if (statusText) {
    let readyText = i18n["chat.ready"][lang];
    if (!readyText && i18n["chat.ready"]["en"]) {
      readyText = await translateText(i18n["chat.ready"]["en"], lang);
    }
    if (readyText) statusText.textContent = readyText;
  }
}

// Initial detection
document.addEventListener("DOMContentLoaded", () => {
  currentLang = detectBrowserLang();
  window.currentLang = currentLang;
  applyLanguage(currentLang);
});


